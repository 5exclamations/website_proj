function currentLang() { return localStorage.getItem("lang") || "ru"; }
function setLang(lang) { localStorage.setItem("lang", lang); render(); }
function el(id) { return document.getElementById(id); }
function imgPath(name) { return encodeURI(`img/${name}`); }

function renderNav(t, page) {
  const nav = el("main-nav");
  if (!nav) return;
  nav.innerHTML = links.map((l) => `<a href="${l.href}" class="${page === l.key ? "active" : ""}">${t.nav[l.key]}</a>`).join("");
  document.querySelectorAll(".lang-switch button").forEach((b) => {
    b.classList.toggle("active", b.dataset.lang === currentLang());
    b.onclick = () => setLang(b.dataset.lang);
  });
}

function renderFooter(t) {
  if (el("footer-copy")) el("footer-copy").textContent = t.footer.copy;
  if (el("footer-links")) el("footer-links").innerHTML = `<a href="#">${t.footer.terms}</a><a href="#">${t.footer.privacy}</a><a href="#">${t.footer.cookies}</a>`;
}

function fillList(id, data) { const node = el(id); if (node) node.innerHTML = data.map((x) => `<li>${x}</li>`).join(""); }

