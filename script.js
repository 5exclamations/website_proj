function render() {
  const lang = currentLang();
  const t = site[lang] || site.ru;
  document.documentElement.lang = lang;
  const pageMap = { "index.html": "home", "bac-therapy.html": "bac", "services.html": "services", "about.html": "about", "contact.html": "contact" };
  const file = window.location.pathname.split("/").pop() || "index.html";
  const page = pageMap[file] || document.body.dataset.page || "home";
  renderNav(t, page);
  renderFooter(t);
  renderHome(t);
  renderBac(t);
  renderServices(t);
  renderAbout(t);
  renderContact(t);
}

